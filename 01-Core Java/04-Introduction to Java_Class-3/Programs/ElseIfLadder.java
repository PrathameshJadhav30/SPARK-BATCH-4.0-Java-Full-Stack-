class ElseIfLadder 
{
	public static void main(String[] args) 
	{
		int num = 75;

        if (num >= 90) {
            System.out.println("Grade: A");
        } else if (num >= 80) {
            System.out.println("Grade: B");
        } else if (num >= 70) {
            System.out.println("Grade: C");
        } else if (num >= 60) {
            System.out.println("Grade: D");
        } else {
            System.out.println("Grade: F");
        }
	}
}

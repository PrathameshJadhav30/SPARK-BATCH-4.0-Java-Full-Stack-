public class AssignmentOperators {
    public static void main(String[] args) {
        int x = 10;
        x += 5; //15
        System.out.println("x += 5: " + x);
        x -= 3; //12
        System.out.println("x -= 3: " + x);
        x *= 2; //24
        System.out.println("x *= 2: " + x);
        x /= 4; //6
        System.out.println("x /= 4: " + x);
        x %= 5; //1
        System.out.println("x %= 5: "+ x);
    }
}
